  }) : id = '', // Empty ID to be auto-generated by Firestore
       employeeId =
           employeeId ??
           '', // Empty employee ID to be auto-generated by service
       createdAt = createdAt ?? DateTime.now();

  // Calculate years of service
  int get yearsOfService {
    final endDate = dateOfLeaving ?? DateTime.now();
    return endDate.difference(dateOfJoining).inDays ~/ 365;
  }

  // Check if currently employed
  bool get isActive => employmentStatus == EmploymentStatus.active;

  // Firestore serialization
  Map<String, dynamic> toFirestore() {
    return {
      'userId': userId,
      'employeeId': employeeId,
      'fullName': fullName,
      'photoUrl': photoUrl,
      'photoScale': photoScale,
      'photoOffsetX': photoOffsetX,
      'photoOffsetY': photoOffsetY,
      'email': email,
      'phoneNumber': phoneNumber,
      'address': address,
      'emergencyContactName': emergencyContactName,
      'emergencyContactPhone': emergencyContactPhone,
      'dateOfBirth': dateOfBirth != null
          ? firestore.Timestamp.fromDate(dateOfBirth!)
          : null,
      'gender': gender?.name,
      'nationalIdNumber': nationalIdNumber,
      'passportNumber': passportNumber,
      'country': country,
      'provinceState': provinceState,
      'department': department,
      'position': position,
      'role': role.name,
      'employmentType': employmentType.name,
      'employmentStatus': employmentStatus.name,
      'dateOfJoining': firestore.Timestamp.fromDate(dateOfJoining),
      'dateOfLeaving': dateOfLeaving != null
          ? firestore.Timestamp.fromDate(dateOfLeaving!)
          : null,
      'reportingManagerId': reportingManagerId,
      'bankAccountNumber': bankAccountNumber,
      'bankName': bankName,
      'taxId': taxId,
      'monthlySalary': monthlySalary,
      'currentSalaryBenefitsId': currentSalaryBenefitsId,
      'allowances': allowances,
      'tithePercentage': tithePercentage,
      'titheAmount': titheAmount,
      'socialSecurityAmount': socialSecurityAmount,
      'providentFundPercentage': providentFundPercentage,
      'providentFundAmount': providentFundAmount,
      'approvalLimit': approvalLimit,
      'permissions': permissions,
      'documentsCount': documentsCount,
      'createdAt': firestore.Timestamp.fromDate(createdAt),
      'updatedAt': updatedAt != null
          ? firestore.Timestamp.fromDate(updatedAt!)
          : null,
      'notes': notes,
    };
  }

  factory Staff.fromFirestore(
    firestore.DocumentSnapshot<Map<String, dynamic>> doc,
  ) {
    final data = doc.data();
    if (data == null) {
      throw Exception('Staff document ${doc.id} has no data');
    }

    // Parse dateOfJoining with fallback
    DateTime dateOfJoining;
    if (data['dateOfJoining'] != null && data['dateOfJoining'] is firestore.Timestamp) {
      dateOfJoining = (data['dateOfJoining'] as firestore.Timestamp).toDate();
    } else {
      dateOfJoining = DateTime.now();
    }

    // Parse createdAt with fallback
    DateTime createdAt;
    if (data['createdAt'] != null && data['createdAt'] is firestore.Timestamp) {
      createdAt = (data['createdAt'] as firestore.Timestamp).toDate();
    } else {
      createdAt = DateTime.now();
    }

    return Staff(
      id: doc.id,
      userId: data['userId'] as String?,
      employeeId: data['employeeId'] as String? ?? '',
      fullName: data['fullName'] as String? ?? 'Unknown',
      photoUrl: data['photoUrl'] as String?,
      photoScale: (data['photoScale'] as num?)?.toDouble(),
      photoOffsetX: (data['photoOffsetX'] as num?)?.toDouble(),
      photoOffsetY: (data['photoOffsetY'] as num?)?.toDouble(),
      email: data['email'] as String? ?? '',
      phoneNumber: data['phoneNumber'] as String?,
      address: data['address'] as String?,
      emergencyContactName: data['emergencyContactName'] as String?,
      emergencyContactPhone: data['emergencyContactPhone'] as String?,
      dateOfBirth: data['dateOfBirth'] != null && data['dateOfBirth'] is firestore.Timestamp
          ? (data['dateOfBirth'] as firestore.Timestamp).toDate()
          : null,
      gender: data['gender'] != null
          ? Gender.values.firstWhere((e) => e.name == data['gender'], orElse: () => Gender.other)
          : null,
      nationalIdNumber: (data['nationalIdNumber'] as String?)?.isNotEmpty == true
          ? data['nationalIdNumber'] as String
          : null,
      passportNumber: (data['passportNumber'] as String?)?.isNotEmpty == true
          ? data['passportNumber'] as String
          : null,
      country: (data['country'] as String?)?.isNotEmpty == true
          ? data['country'] as String
          : null,
      provinceState: (data['provinceState'] as String?)?.isNotEmpty == true
          ? data['provinceState'] as String
          : null,
      department: data['department'] as String? ?? 'Unknown',
      position: data['position'] as String? ?? 'Unknown',
      role: UserRole.values.firstWhere(
        (e) => e.name == data['role'],
        orElse: () => UserRole.requester,
      ),
      employmentType: EmploymentType.values.firstWhere(
        (e) => e.name == data['employmentType'],
        orElse: () => EmploymentType.fullTime,
      ),
      employmentStatus: EmploymentStatus.values.firstWhere(
        (e) => e.name == data['employmentStatus'],
        orElse: () => EmploymentStatus.active,
      ),
      dateOfJoining: dateOfJoining,
      dateOfLeaving: data['dateOfLeaving'] != null && data['dateOfLeaving'] is firestore.Timestamp
          ? (data['dateOfLeaving'] as firestore.Timestamp).toDate()
          : null,
      reportingManagerId: data['reportingManagerId'] as String?,
      bankAccountNumber: data['bankAccountNumber'] as String?,
      bankName: data['bankName'] as String?,
      taxId: data['taxId'] as String?,
      monthlySalary: data['monthlySalary']?.toDouble(),
      currentSalaryBenefitsId: data['currentSalaryBenefitsId'] as String?,
      allowances: data['allowances']?.toDouble(),
      tithePercentage: data['tithePercentage']?.toDouble(),
      titheAmount: data['titheAmount']?.toDouble(),
      socialSecurityAmount: data['socialSecurityAmount']?.toDouble(),
      providentFundPercentage: data['providentFundPercentage']?.toDouble(),
      providentFundAmount: data['providentFundAmount']?.toDouble(),
      approvalLimit: data['approvalLimit']?.toDouble(),
      permissions: data['permissions'] != null
          ? List<String>.from(data['permissions'] as List)
          : [],
      documentsCount: data['documentsCount'] as int? ?? 0,
      createdAt: createdAt,
      updatedAt: data['updatedAt'] != null && data['updatedAt'] is firestore.Timestamp
          ? (data['updatedAt'] as firestore.Timestamp).toDate()
          : null,
      notes: data['notes'] as String?,
    );
  }

