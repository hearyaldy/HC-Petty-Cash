import 'package:cloud_firestore/cloud_firestore.dart' as firestore;
import 'enums.dart';

class Staff {
  final String id; // Auto-generated ID
  final String?
  userId; // Link to User model (Firebase Auth UID) - optional for non-system users
  final String employeeId; // Custom employee ID (e.g., EMP001)
  final String fullName;
  final String? photoUrl; // Profile photo URL from Firebase Storage
  final double? photoScale; // Avatar zoom level
  final double? photoOffsetX; // Avatar horizontal offset (-1..1)
  final double? photoOffsetY; // Avatar vertical offset (-1..1)

  // Contact Information
  final String email;
  final String? phoneNumber;
  final String? address;
  final String? emergencyContactName;
  final String? emergencyContactPhone;

  // Personal Information
  final DateTime? dateOfBirth;
  final Gender? gender;
  final String? nationalIdNumber;
  final String? passportNumber;
  final String? country;
  final String? provinceState;

  // Employment Details
  final String department;
  final String position; // Job title
  final UserRole role; // System role (requester, manager, finance, admin)
  final EmploymentType employmentType;
  final EmploymentStatus employmentStatus;
  final DateTime dateOfJoining;
  final DateTime? dateOfLeaving;
  final String? reportingManagerId; // Reference to another Staff ID

  // Financial Information
  final String? bankAccountNumber;
  final String? bankName;
  final String? taxId;
  final double?
  monthlySalary; // Base salary (deprecated - use SalaryBenefits model instead)
  final String?
  currentSalaryBenefitsId; // Reference to current salary benefits record

  // Financial Information
  final double? allowances;
  final double? tithePercentage;
  final double? titheAmount;
  final double? socialSecurityAmount;
  final double? providentFundPercentage;
  final double? providentFundAmount;

  // Authorization & Access
  final double? approvalLimit; // Maximum amount they can approve for vouchers
  final List<String> permissions; // Additional permissions if needed

  // Documents
  final int documentsCount; // Count of uploaded documents (managed separately)

  // Metadata
  final DateTime createdAt;
  final DateTime? updatedAt;
  final String? notes; // Additional notes about the staff member

  Staff({
    required this.id,
    this.userId,
    required this.employeeId,
    required this.fullName,
    this.photoUrl,
    this.photoScale,
    this.photoOffsetX,
    this.photoOffsetY,
    required this.email,
    this.phoneNumber,
    this.address,
    this.emergencyContactName,
    this.emergencyContactPhone,
    this.dateOfBirth,
    this.gender,
    this.nationalIdNumber,
    this.passportNumber,
    this.country,
    this.provinceState,
    required this.department,
    required this.position,
    required this.role,
    required this.employmentType,
    required this.employmentStatus,
    required this.dateOfJoining,
    this.dateOfLeaving,
    this.reportingManagerId,
    this.bankAccountNumber,
    this.bankName,
    this.taxId,
    this.monthlySalary,
    this.currentSalaryBenefitsId,
    this.allowances,
    this.tithePercentage,
    this.titheAmount,
    this.socialSecurityAmount,
    this.providentFundPercentage,
    this.providentFundAmount,
    this.approvalLimit,
    this.permissions = const [],
    this.documentsCount = 0,
    required this.createdAt,
    this.updatedAt,
    this.notes,
  });

  // Factory constructor for creating new staff (ID will be auto-generated by Firestore)
  Staff.create({
    this.userId,
    String? employeeId,
    required this.fullName,
    this.photoUrl,
    this.photoScale,
    this.photoOffsetX,
    this.photoOffsetY,
    required this.email,
    this.phoneNumber,
    this.address,
    this.emergencyContactName,
    this.emergencyContactPhone,
    this.dateOfBirth,
    this.gender,
    this.nationalIdNumber,
    this.passportNumber,
    this.country,
    this.provinceState,
    required this.department,
    required this.position,
    required this.role,
    required this.employmentType,
    required this.employmentStatus,
    required this.dateOfJoining,
    this.dateOfLeaving,
    this.reportingManagerId,
    this.bankAccountNumber,
    this.bankName,
    this.taxId,
    this.monthlySalary,
    this.currentSalaryBenefitsId,
    this.allowances,
    this.tithePercentage,
    this.titheAmount,
    this.socialSecurityAmount,
